from pathlib import Path

from docx import Document
from docx.shared import Pt


def set_run_font(run, size_pt=11):
    run.font.name = "Microsoft YaHei"
    run.font.size = Pt(size_pt)


def set_heading_run_font(run, size_pt=14):
    run.font.name = "Microsoft YaHei"
    run.font.size = Pt(size_pt)


def add_para(doc, text, size_pt=11):
    p = doc.add_paragraph(text)
    for r in p.runs:
        set_run_font(r, size_pt)


def add_heading(doc, text, level=1):
    h = doc.add_heading(text, level=level)
    for r in h.runs:
        set_heading_run_font(r, 16 if level == 1 else 13)
    return h


def _style_table_cell(cell, text, size_pt=10):
    cell.text = text
    for p in cell.paragraphs:
        for r in p.runs:
            set_run_font(r, size_pt)


def add_distributor_level_simple_table(doc, data_rows=12):
    """
    分销等级参数表：与用户文档一致的 8 列表头 + 若干空行供填写。
    """
    headers = [
        "序号",
        "等级排序（整数）",
        "等级名称",
        "提货成本率（%）",
        "成本按何计算\n（零售价/成本价）",
        "直接分享比例\n（0~1）",
        "间接分享比例\n（0~1）",
        "备注",
    ]
    table = doc.add_table(rows=1 + data_rows, cols=len(headers))
    table.style = "Table Grid"
    for c, h in enumerate(headers):
        _style_table_cell(table.rows[0].cells[c], h, 8)
    for r in range(1, 1 + data_rows):
        _style_table_cell(table.rows[r].cells[0], str(r), 9)
        for c in range(1, len(headers)):
            _style_table_cell(table.rows[r].cells[c], "—", 9)

    add_heading(doc, "表头说明：各列含义", level=3)
    col_help = [
        "「序号」：表格中的行号，便于查阅、沟通及与内部台账对应；不参与任何金额或比例计算。",
        "「等级排序（整数）」：平台为各分销档位设定的顺序编号，用于区分高低或先后。同一制度下数字与「哪一档更高」的对应关系（如数值越大等级越高、或相反）以贵司公示规则为准，填写整数。",
        "「等级名称」：对外展示的分销合作档位名称（如区域伙伴、专卖档等），须与小程序、合同或招商资料中的称谓保持一致，便于伙伴对号入座。",
        "「提货成本率（%）」：该档位在订货/提货时，在下一列所选定计价基数上需承担的成本比例，以百分数填写（如 70 表示 70%）。一般而言，在相同计价口径下，该比例越低，通常表示该档在供货成本上越有优势；是否与推广奖励合并计算，以平台规则为准。",
        "「成本按何计算（零售价/成本价）」：说明「提货成本率」所乘的计价基数。填「零售价」表示按商品参考零售价（或平台规定的零售类口径）计算提货成本；填「成本价」表示按商品成本合计（或平台规定的成本类口径）计算。须与后台实际配置一致，二者勿混用。",
        "「直接分享比例（0~1）」：因您**直接推广**而成交时，适用于您的分享奖励比例，请填 **0 到 1 之间的小数**（例如 0.05 表示 5%）。若界面以「百分比」展示，请换算为同一口径后再填入本表。",
        "「间接分享比例（0~1）」：在符合平台多级推荐规则的前提下，因**间接推广链条**带来的成交，适用于您的分享奖励比例，同样为 0~1 小数。若本档不享受间接分享或未启用该功能，可填 0 或注明「不适用」。",
        "「备注」：可填写该档升级条件、考核周期、区域或品类限制、与限时活动叠加以何者优先等；无补充内容时可填「—」或留空。",
    ]
    for line in col_help:
        p = doc.add_paragraph(line, style="List Bullet")
        for r in p.runs:
            set_run_font(r, 10)


